clc;

addpath('elasticity')
addpath('elasticity/materials')

addpath('shape')
addpath('plotters')
addpath('solvers')

x   = [0 0; 1 0; 2 0; 3 0; 0 1; 1 1; 2 1; 3 1 ; 0 2; 1 2; 2 2; 3 2; 0 3;...
    1 3; 2 3; 3 3]/3;
x0  = [1 1 0; 1 2 0; 2 2 0; 3 2 0; 4 2 0 ; 5 1 0; 9 1 0; 13 1 0 ];
t   = [0 0; 0 0; 0 0; 5 0; 0 0; 0 0; 0 0;10 0 ; 0 0; 0 0; 0 0;10 0; ...
    0 0; 0 0; 0 0; 5 0];
X = x;

n = [1 2 6 5; 2 3 7 6; 3 4 8 7; 5 6 10 9; 6 7 11 10; 7 8 12 11; ...
    9 10 14 13; 10 11 15 14; 11 12 16 15];

P = [  100 0.3 1; 100 0.3 1; 100 0.3 1; 100 0.3 1; 100 0.3 1; 100 0.3 1;... 
    100 0.3 1; 100 0.3 1; 100 0.3 1];

K = stiffK(x, X, P, n, 'venant');  
K = setboundsK(K, x0, n);

te = t';
te = te(:);

u = K\te(:);
u = reshape(u, [size(x,2), size(x,1)])';

femplot(X,x+u,n);
