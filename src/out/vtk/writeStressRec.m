function stress_str = writeStressRec(stresses)
    stress_str = sprintf("TENSORS Stress float \n");
    for a = 1:size(stresses,1)
        stress_str = stress_str + sprintf("%.9f %.9f %.9f \n"+...
                                          "%.9f %.9f %.9f \n"+...
                                          "%.9f %.9f %.9f \n\n",...
                            stresses(a,1),stresses(a,4),stresses(a,5),...
                            stresses(a,4),stresses(a,2),stresses(a,6),...
                            stresses(a,5),stresses(a,6),stresses(a,3));
    end
end